import { summarise } from './chart'

const input = {
  'a': [
    {
      x: '2018-01-19T12:00:00.000000Z',
      y: 100
    },
    {
      x: '2018-01-19T13:00:00.000000Z',
      y: 200,
    },

    {
      x: '2018-01-20T13:00:00.000000Z',
      y: 300,
    },
    {
      x: '2018-01-20T18:00:00.000000Z',
      y: 400,
    },
    {
      x: '2018-01-20T19:00:00.000000Z',
      y: 500,
    },

    {
      x: '2018-02-20T19:00:00.000000Z',
      y: 600,
    },
  ]
};

const expected = {
  'a': [
    {
      x: '2018-01-19T13:00:00.000000Z',
      y: 200,
    },
    {
      x: '2018-01-20T19:00:00.000000Z',
      y: 500,
    },
    {
      x: '2018-02-20T19:00:00.000000Z',
      y: 600,
    }
  ]
};

test('summarise', () => {
  expect(summarise(input)).toEqual(expected);
});